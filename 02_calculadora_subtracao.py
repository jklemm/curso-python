print('Calculadora que faz Subtracao')

numero_1 = input('Informe o primeiro numero: ')

numero_2 = input('Informe o segundo numero: ')

numero_3 = input('Informe o terceiro numero: ')

resultado = int(numero_1) - int(numero_2) - int(numero_3)

print('A subtracao desses numeros eh: {}'.format(resultado))

print('Ou seja, {} - {} - {} = {}'.format(numero_1, numero_2, numero_3, resultado))
